#include "push_swap.h"

t_stack	*ft_lstnew(int content)
{
	t_stack	*node;

	node = NULL;
	node = (t_stack *)malloc(sizeof(t_stack));
	if (!node)
		return (NULL);
	node->content = content;
	node->next = NULL;
	return (node);
}

void	ft_lstadd_front(t_stack **lst, t_stack *new)
{
	if (lst && new)
	{
		new->next = *lst;
		*lst = new;
	}
}

t_stack	*ft_lstlast(t_stack *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}

void	ft_lstadd_back(t_stack **lst, t_stack *new)
{
	t_stack	*lis;

	if (!lst || !new)
		return ;
	if (*lst)
	{
		lis = ft_lstlast(*lst);
		lis->next = new;
	}
	else
		(*lst) = new;
}

int	ft_lstsize(t_stack *lst)
{
	int	i;

	i = 0;
	if (lst)
	{
		while (lst)
		{
			lst = lst->next;
			i++;
		}
	}
	return (i);
}
